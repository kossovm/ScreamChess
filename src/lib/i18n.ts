export type TranslationKey = keyof typeof dict.en;

export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
  const table = dict[locale] ?? dict.en;
  let s: string = (table as Record<string, string>)[key] ?? (dict.en as Record<string, string>)[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return s;
}

export function detectLocale(): Locale {
  if (typeof navigator === 'undefined') return 'en';
  const lang = (navigator.language || 'en').toLowerCase();
  if (lang.startsWith('ru') || lang.startsWith('uk') || lang.startsWith('be') || lang.startsWith('kk')) return 'ru';
  return 'en';
}
